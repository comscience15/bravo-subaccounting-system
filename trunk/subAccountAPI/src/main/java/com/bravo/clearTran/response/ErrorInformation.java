package com.bravo.clearTran.response;

public class ErrorInformation {
	private String Text;
	private int Number;
	
	public String getText() {
		return Text;
	}
	public void setText(String text) {
		Text = text;
	}
	public int getNumber() {
		return Number;
	}
	public void setNumber(int number) {
		Number = number;
	}
	
	
}
