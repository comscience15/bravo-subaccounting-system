package com.bravo.webapp.bean;

public class PublicKeyBean {
	private String e;
	private String n;
	private String maxdigits;

	public String getE() {
		return e;
	}

	public void setE(String e) {
		this.e = e;
	}

	public String getN() {
		return n;
	}

	public void setN(String n) {
		this.n = n;
	}

	public String getMaxdigits() {
		return maxdigits;
	}

	public void setMaxdigits(String maxdigits) {
		this.maxdigits = maxdigits;
	}

}
